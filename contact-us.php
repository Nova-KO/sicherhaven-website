    }),
    onLeave: batch => gsap.set(batch, {
        opacity: 0,
        y: 100,
        overwrite: true
    }),
    onEnterBack: batch => gsap.to(batch, {
        opacity: 1,
        y: 0,
        stagger: 0.15,
        overwrite: true
    }),
    onLeaveBack: batch => gsap.set(batch, {
        opacity: 0,
        y: -100,
        overwrite: true
    })
});

// Update on window resize
window.addEventListener("resize", () => {
    ScrollTrigger.refresh();
});
